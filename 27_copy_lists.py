# Copy Lists
# Use the copy() method

thislist = ["apple", "banana", "cherry"]
list1 = thislist.copy()
print(list1)

# Use the list() method
tlist = ["apple", "banana", "cherry"]
mylist = list(tlist)
print(mylist)

# Use the slice Operator
thislist = ["apple", "banana", "cherry"]
mylist2 = thislist[:]
print(mylist2)

